class Patient:

    """generic patient class"""

    def __init__(self, firstName, middleInit, lastName, age, gender, dob):

        self.firstName = firstName
        self.middleInit = middleInit
        self.lastName = lastName
        self.age = age
        self.gender = gender
        self. dob = dob

class Staff:

    """generic staff class"""

    def __init__(self, firstName, lastName, job):

        self.firstName = firstName
        self.lastName = lastName
        self.job = job

